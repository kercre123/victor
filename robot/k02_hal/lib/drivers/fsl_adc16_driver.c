/*FUNCTION*********************************************************************
 *
 * Function Name : ADC16_DRV_ConvRAWData
 * Description   : Format the initial value fetched from ADC module. 
 * Initial value fetched from ADC module can not be read as an normal number,
 * especially for signed value generated by differential conversion. This
 * function can format the initial value to be a readable one.
 *
 *END*************************************************************************/
int32_t ADC16_DRV_ConvRAWData(uint16_t convValue, bool diffEnable, adc16_resolution_mode_t mode)
{
    uint32_t ret32 = 0U;
    switch (mode)
    {
#if (FSL_FEATURE_ADC16_MAX_RESOLUTION >= 16)
    case kAdcResolutionBitOf16:
        if (!diffEnable)
        {
            ret32 = convValue & ADC16_CONV_DATA16_MASK; /* Get Data. */
        }
        else
        {
            ret32 = convValue & ADC16_CONV_DATA15_MASK; /* Get Data. */
            ret32 |= ( (convValue & ADC16_CONV_SIGN15_MASK) ? (ADC16_CONV_SIGN15_MASK|ADC16_CONV_SIGN32_MASK):0); /* Get sign. */
        }
        break;
#endif /* FSL_FEATURE_ADC16_MAX_RESOLUTION */
    case kAdcResolutionBitOf12or13:
        /* Get Data. */
        ret32 = convValue & ADC16_CONV_DATA12_MASK;
        if (diffEnable)

        {
            ret32 |= ( (convValue & ADC16_CONV_SIGN12_MASK) ? (ADC16_CONV_SIGN12_MASK|ADC16_CONV_SIGN32_MASK):0); /* Get sign. */
        }
        break;
    case kAdcResolutionBitOf10or11:
        /* Get Data. */
        ret32 = convValue & ADC16_CONV_DATA10_MASK;
        if (diffEnable)
        {
            ret32 |= ( (convValue & ADC16_CONV_SIGN10_MASK) ? (ADC16_CONV_SIGN10_MASK|ADC16_CONV_SIGN32_MASK):0); /* Get sign. */
        }
        break;
    case kAdcResolutionBitOf8or9:
        /* Get Data. */
        ret32 = convValue & ADC16_CONV_DATA8_MASK;
        if (diffEnable)
        {
            ret32 |= ( (convValue & ADC16_CONV_SIGN8_MASK) ? (ADC16_CONV_SIGN8_MASK|ADC16_CONV_SIGN32_MASK):0); /* Get sign. */
        }
        break;
    default:
        break;    
    }
    return (int32_t)(ret32);
}

/*FUNCTION*********************************************************************
 *
 * Function Name : ADC16_DRV_GetConvFlag
 * Description   : Get the status of event of ADC converter.
 * If the event is asserted, it will return "true", otherwise will be "false".
 *
 *END*************************************************************************/
bool ADC16_DRV_GetConvFlag(uint32_t instance, adc16_flag_t flag)
{
    assert(instance < HW_ADC_INSTANCE_COUNT);
    uint32_t baseAddr = g_adcBaseAddr[instance];
    
    bool bRet = false;
    switch (flag)
    {
    case kAdcConvActiveFlag:
        bRet = ADC16_HAL_GetConvActiveCmd(baseAddr);
        break;
#if FSL_FEATURE_ADC16_HAS_CALIBRATION
    case kAdcCalibrationFailedFlag:
        bRet = ADC16_HAL_GetAutoCalibrationFailedCmd(baseAddr);
        break;
    case kAdcCalibrationActiveFlag:
        bRet = ADC16_HAL_GetAutoCalibrationActiveCmd(baseAddr);
        break;
#endif /* FSL_FEATURE_ADC16_HAS_CALIBRATION */
    default:
        break;
    }
    return bRet;
}

/*FUNCTION*********************************************************************
 *
 * Function Name : ADC16_DRV_GetChnFlag
 * Description   : Get the status of event of each ADC channel group.
 * If the event is asserted, it will return "true", otherwise will be "false".
 *
 *END*************************************************************************/
bool ADC16_DRV_GetChnFlag(uint32_t instance, uint32_t chnGroup, adc16_flag_t flag)
{
    assert(instance < HW_ADC_INSTANCE_COUNT);
    uint32_t baseAddr = g_adcBaseAddr[instance];
    
    bool bRet = false;
    switch (flag)
    {
    case kAdcChnConvCompleteFlag:
        bRet = ADC16_HAL_GetChnConvCompletedCmd(baseAddr, chnGroup);
        break;
    default:
        break;
    }
    return bRet;
}

