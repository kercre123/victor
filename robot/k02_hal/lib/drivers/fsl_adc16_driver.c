/*FUNCTION*********************************************************************
 *
 * Function Name : ADC16_DRV_EnableHwCmp
 * Description   : Initialize the feature of long sample mode in ADC
 * module. This API would enable the feature of hardware compare with
 * indicated configuration. Launch the hardware compare would make the
 * conversion result in predefined range can be only accepted. Values out of
 * range would be ignored during conversion.
 *
 *END*************************************************************************/
adc16_status_t ADC16_DRV_EnableHwCmp(uint32_t instance, adc16_hw_cmp_config_t *hwCmpConfigPtr)
{
    assert(instance < HW_ADC_INSTANCE_COUNT);
    uint32_t baseAddr = g_adcBaseAddr[instance];

    if (!hwCmpConfigPtr)
    {
        return kStatus_ADC16_InvalidArgument;
    }

    /* Enable CMP feature. */
    ADC16_HAL_SetHwCmpCmd(baseAddr, true);

    /* Set CMP range mode. */
    ADC16_HAL_SetHwCmpMode(baseAddr, hwCmpConfigPtr->cmpRangeMode);

    /* Set CMP values. */
    ADC16_HAL_SetHwCmpValue1(baseAddr, hwCmpConfigPtr->cmpValue1);
    ADC16_HAL_SetHwCmpValue2(baseAddr, hwCmpConfigPtr->cmpValue2);
    
    return kStatus_ADC16_Success;
}

