/* Define MACRO for formatting the ADC16 result value. */
#define ADC16_CONV_SIGN32_MASK    (0xFFFF0000U)
#if (FSL_FEATURE_ADC16_MAX_RESOLUTION >= 16)
#define ADC16_CONV_DATA16_MASK    (0xFFFFU)
#define ADC16_CONV_DATA15_MASK    (0x7FFFU)
#define ADC16_CONV_SIGN15_MASK    (0x8000U)
#endif /* FSL_FEATURE_ADC16_MAX_RESOLUTION */
#define ADC16_CONV_DATA12_MASK    (0x0FFFU)
#define ADC16_CONV_SIGN12_MASK    (0xF000U)
#define ADC16_CONV_DATA10_MASK    (0x03FFU)
#define ADC16_CONV_SIGN10_MASK    (0xFC00U)
#define ADC16_CONV_DATA8_MASK     (0x00FFU)
#define ADC16_CONV_SIGN8_MASK     (0xFF00U)

#if FSL_FEATURE_ADC16_HAS_CALIBRATION

/*FUNCTION*********************************************************************
 *
 * Function Name : ADC16_DRV_GetAutoCalibrationParam
 * Description   : Execute the the process of auto calibration and fetch
 * the calibration parameters that would be kept in "adc16_calibration_param_t"
 * type variable. When executing the process of auto calibration, the ADC
 * has been configured internally to work in the situation with highest
 * precision. Since this API may be called before the initialization, it enables
 * the ADC clock internally.
 *
 *END*************************************************************************/
adc16_status_t ADC16_DRV_GetAutoCalibrationParam(uint32_t instance, adc16_calibration_param_t *paramPtr)
{
    assert(instance < HW_ADC_INSTANCE_COUNT);

    uint16_t dummy = 0U;
    uint32_t baseAddr = g_adcBaseAddr[instance];
    
    dummy = dummy; /* Avoid warning when in compiling. */
    if (!paramPtr)
    {
        return kStatus_ADC16_InvalidArgument;
    }
    /* Special configuration for highest accuracy. */
    /* Enable clock for ADC. */
    if (!CLOCK_SYS_GetAdcGateCmd(instance))
    {
        CLOCK_SYS_EnableAdcClock(instance);
    }

    /* Maximum Average. */
#if FSL_FEATURE_ADC16_HAS_HW_AVERAGE
    ADC16_HAL_SetHwAverageCmd(baseAddr, true);
    ADC16_HAL_SetHwAverageMode(baseAddr, kAdcHwAverageCountOf32);
#endif /* FSL_FEATURE_ADC16_HAS_HW_AVERAGE */

    /* Lowest ADC Frequency. */
    ADC16_HAL_SetClkSrcMode(baseAddr, kAdcClkSrcOfBusClk);
    ADC16_HAL_SetClkDividerMode(baseAddr, kAdcClkDividerInputOf8);

    /* Reference voltage as Vadd. */
    ADC16_HAL_SetRefVoltSrcMode(baseAddr, kAdcRefVoltSrcOfVref);

    /* Software trigger. */
    ADC16_HAL_SetHwTriggerCmd(baseAddr, false);

    ADC16_HAL_SetAutoCalibrationCmd(baseAddr, true); /* Launch auto calibration. */
    while ( !ADC16_HAL_GetChnConvCompletedCmd(baseAddr, 0U) )
    {
        if ( ADC16_HAL_GetAutoCalibrationFailedCmd(baseAddr) )
        {
            return kStatus_ADC16_Failed;
        }
    }
    /* Read parameters that generated by auto calibration. */
#if FSL_FEATURE_ADC16_HAS_OFFSET_CORRECTION
    paramPtr->offsetValue = ADC16_HAL_GetOffsetValue(baseAddr);
#endif /* FSL_FEATURE_ADC16_HAS_OFFSET_CORRECTION */
    paramPtr->plusSideGainValue = ADC16_HAL_GetAutoPlusSideGainValue(baseAddr);
#if FSL_FEATURE_ADC16_HAS_DIFF_MODE
    paramPtr->minusSideGainValue = ADC16_HAL_GetAutoMinusSideGainValue(baseAddr);
#endif /* FSL_FEATURE_ADC16_HAS_DIFF_MODE */
    dummy = ADC16_HAL_GetChnConvValueRAW(baseAddr, 0U);
    
    ADC16_HAL_SetAutoCalibrationCmd(baseAddr, false); /* Terminate auto calibration. */

    return kStatus_ADC16_Success;
}

/*FUNCTION*********************************************************************
 *
 * Function Name : ADC16_DRV_SetCalibrationParam
 * Description   : Set the calibration parameters for ADC module. These
 * parameters can be fetched by launching the process of auto calibration
 * or to use some predefined values that filled in the structure of 
 * "adc16_calibration_param_t". For higher precision,  it is recommended to
 * execute the process of calibration before initializing the ADC module.
 * Since this API may be called before the initialization, it enables the ADC
 * clock internally.
 *
 *END*************************************************************************/
adc16_status_t ADC16_DRV_SetCalibrationParam(uint32_t instance, adc16_calibration_param_t *paramPtr)
{
    assert(instance < HW_ADC_INSTANCE_COUNT);

    uint32_t baseAddr = g_adcBaseAddr[instance];
    
    if (!paramPtr)
    {
        return kStatus_ADC16_InvalidArgument;
    }
    /* Special configuration for highest accuracy. */
    /* Enable clock for ADC. */
    if (!CLOCK_SYS_GetAdcGateCmd(instance))
    {
        CLOCK_SYS_EnableAdcClock(instance);
    }

#if FSL_FEATURE_ADC16_HAS_OFFSET_CORRECTION
    ADC16_HAL_SetOffsetValue(baseAddr, paramPtr->offsetValue);
#endif /* FSL_FEATURE_ADC16_HAS_OFFSET_CORRECTION */
    ADC16_HAL_SetPlusSideGainValue(baseAddr, paramPtr->plusSideGainValue);
#if FSL_FEATURE_ADC16_HAS_DIFF_MODE
    ADC16_HAL_SetMinusSideGainValue(baseAddr, paramPtr->minusSideGainValue);
#endif /* FSL_FEATURE_ADC16_HAS_DIFF_MODE */

    return kStatus_ADC16_Success;
}

#endif /* FSL_FEATURE_ADC16_HAS_CALIBRATION */

/*FUNCTION*********************************************************************
 *
 * Function Name : ADC16_DRV_StructInitUserConfigDefault
 * Description   : Fills the initial user configuration defaultly for a one-time
 * trigger mode. Calling the initialization function with the filled parameter
 * configures the ADC module work as one-time trigger mode.
 *
 *END*************************************************************************/
adc16_status_t ADC16_DRV_StructInitUserConfigDefault(adc16_user_config_t *userConfigPtr)
{
    if ( !userConfigPtr )
    {
        return kStatus_ADC16_InvalidArgument;
    }

    userConfigPtr->intEnable = false;
    userConfigPtr->lowPowerEnable = false;
    userConfigPtr->clkDividerMode = kAdcClkDividerInputOf8;
    userConfigPtr->resolutionMode = kAdcResolutionBitOf12or13;
    userConfigPtr->clkSrcMode = kAdcClkSrcOfBusClk;
    userConfigPtr->asyncClkEnable = false;
    userConfigPtr->highSpeedEnable = false;
    userConfigPtr->hwTriggerEnable = false;
#if FSL_FEATURE_ADC16_HAS_DMA
    userConfigPtr->dmaEnable = false;
#endif /* FSL_FEATURE_ADC16_HAS_DMA */
    userConfigPtr->refVoltSrcMode = kAdcRefVoltSrcOfVref;
    userConfigPtr->continuousConvEnable = false;

    return kStatus_ADC16_Success;
}

/*FUNCTION*********************************************************************
 *
 * Function Name : ADC16_DRV_Init
 * Description   : Initialize the comparator in ADC module. No mater if the
 * calibration has been done for the device, this API with initial configuration
 * should be called before any other operations to the ADC module. In fact,
 * these initial configure are mainly for the comparator itself. For advanced
 * feature, responding APIs would be called after this function.
 *
 *END*************************************************************************/
adc16_status_t ADC16_DRV_Init(uint32_t instance, adc16_user_config_t *userConfigPtr)
{
    assert(instance < HW_ADC_INSTANCE_COUNT);

    uint32_t baseAddr = g_adcBaseAddr[instance];

    if (!userConfigPtr)
    {
        return kStatus_ADC16_InvalidArgument;
    }
    /* Enable clock for ADC. */
    CLOCK_SYS_EnableAdcClock(instance);

    /* Reset all the register to a known state. */
    ADC16_HAL_Init(baseAddr);

    /* Low power. */
    ADC16_HAL_SetLowPowerCmd(baseAddr, userConfigPtr->lowPowerEnable);

    /* Clock divider. */
    ADC16_HAL_SetClkDividerMode(baseAddr, userConfigPtr->clkDividerMode);

    /* Conversion resolution mode. */
    ADC16_HAL_SetResolutionMode(baseAddr, userConfigPtr->resolutionMode);

    /* Input clock select. */
    ADC16_HAL_SetClkSrcMode(baseAddr, userConfigPtr->clkSrcMode);

    /* Asynchronous clock output. */
    ADC16_HAL_SetAsyncClkCmd(baseAddr, userConfigPtr->asyncClkEnable);

    /* High speed configuration. */
    ADC16_HAL_SetHighSpeedCmd(baseAddr, userConfigPtr->highSpeedEnable);

    /* Conversion trigger selection. */
    ADC16_HAL_SetHwTriggerCmd(baseAddr, userConfigPtr->hwTriggerEnable);

#if FSL_FEATURE_ADC16_HAS_DMA
    /* DMA. */
    ADC16_HAL_SetDmaCmd(baseAddr, userConfigPtr->dmaEnable);
#endif /* FSL_FEATURE_ADC16_HAS_DMA */

    /* Voltage reference selection. */
    ADC16_HAL_SetRefVoltSrcMode(baseAddr, userConfigPtr->refVoltSrcMode);

    /* Continuous conversion. */
    ADC16_HAL_SetContinuousConvCmd(baseAddr, userConfigPtr->continuousConvEnable);

    /* Interrupt. */
    if (userConfigPtr->intEnable)
    {
        /* Enable ADC interrupt in NVIC level.*/
        INT_SYS_EnableIRQ(g_adcIrqId[instance] );
    }
    else
    {
        /* Disable ADC interrupt in NVIC level.*/
        INT_SYS_DisableIRQ(g_adcIrqId[instance] );
    }

    return kStatus_ADC16_Success;
}

/*FUNCTION*********************************************************************
 *
 * Function Name : ADC16_DRV_Deinit
 * Description   : De-initialize the comparator in ADC module. It will gate
 * the clock to ADC module. When ADC is no long used in application, calling
 * this API will shut down the device to reduce power consumption.
 *
 *END*************************************************************************/
void ADC16_DRV_Deinit(uint32_t instance)
{
    /* Disable ADC interrupt in NVIC level. */
    INT_SYS_DisableIRQ( g_adcIrqId[instance] );

    /* Disable clock for ADC. */
    CLOCK_SYS_DisableAdcClock(instance);
}

